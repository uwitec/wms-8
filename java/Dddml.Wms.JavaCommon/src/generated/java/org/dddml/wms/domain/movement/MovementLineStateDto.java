    public static class DtoConverter extends AbstractStateDtoConverter
    {
        public static Collection<String> collectionFieldNames = Arrays.asList(new String[]{});

        @Override
        protected boolean isCollectionField(String fieldName) {
            return CollectionUtils.collectionContainsIgnoringCase(collectionFieldNames, fieldName);
        }

        public MovementLineStateDto[] toMovementLineStateDtoArray(Iterable<MovementLineState> states) {
            return toMovementLineStateDtoList(states).toArray(new MovementLineStateDto[0]);
        }

        public List<MovementLineStateDto> toMovementLineStateDtoList(Iterable<MovementLineState> states) {
            ArrayList<MovementLineStateDto> stateDtos = new ArrayList();
            for (MovementLineState s : states) {
                MovementLineStateDto dto = toMovementLineStateDto(s);
                stateDtos.add(dto);
            }
            return stateDtos;
        }

        public MovementLineStateDto toMovementLineStateDto(MovementLineState state)
        {
            if(state == null) {
                return null;
            }
            MovementLineStateDto dto = new MovementLineStateDto();
            if (returnedFieldsContains("LineNumber")) {
                dto.setLineNumber(state.getLineNumber());
            }
            if (returnedFieldsContains("MovementQuantity")) {
                dto.setMovementQuantity(state.getMovementQuantity());
            }
            if (returnedFieldsContains("ProductId")) {
                dto.setProductId(state.getProductId());
            }
            if (returnedFieldsContains("LocatorIdFrom")) {
                dto.setLocatorIdFrom(state.getLocatorIdFrom());
            }
            if (returnedFieldsContains("LocatorIdTo")) {
                dto.setLocatorIdTo(state.getLocatorIdTo());
            }
            if (returnedFieldsContains("AttributeSetInstanceId")) {
                dto.setAttributeSetInstanceId(state.getAttributeSetInstanceId());
            }
            if (returnedFieldsContains("Processed")) {
                dto.setProcessed(state.getProcessed());
            }
            if (returnedFieldsContains("ReversalLineNumber")) {
                dto.setReversalLineNumber(state.getReversalLineNumber());
            }
            if (returnedFieldsContains("Active")) {
                dto.setActive(state.getActive());
            }
            if (returnedFieldsContains("Version")) {
                dto.setVersion(state.getVersion());
            }
            if (returnedFieldsContains("MovementDocumentNumber")) {
                dto.setMovementDocumentNumber(state.getMovementDocumentNumber());
            }
            if (returnedFieldsContains("CreatedBy")) {
                dto.setCreatedBy(state.getCreatedBy());
            }
            if (returnedFieldsContains("CreatedAt")) {
                dto.setCreatedAt(state.getCreatedAt());
            }
            if (returnedFieldsContains("UpdatedBy")) {
                dto.setUpdatedBy(state.getUpdatedBy());
            }
            if (returnedFieldsContains("UpdatedAt")) {
                dto.setUpdatedAt(state.getUpdatedAt());
            }
            return dto;
        }

    }
